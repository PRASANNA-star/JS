//inbuilt methods in array
var arr1=[12,14,56,100]

//pop->Removing the last element
console.log(arr1)
arr1.pop()
console.log("Popped: ",arr1)

//push
arr1.push(90)
console.log("pushed: ",arr1)

//shift->Removing the first element
arr1.shift()
console.log("shifted: ",arr1)

//unshift->add the element in the first
arr1.unshift(100)
console.log("Unshifted: ",arr1)
